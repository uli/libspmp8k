//
// I_Error
//
extern boolean demorecording;

void I_Error (char *error, ...)
{
	va_list args;
	char str[256];
	ge_key_data_t keys;
	
	va_start(args, error);
	vsnprintf(str, 255, error, args);

#ifdef DEBUG	
//	dmsg_clear();
	dmsg_puts("I_Error: ");
	dmsg_puts(str);
	dmsg_puts("\nPress 'O' to exit");
#endif
	// wait until 'O' is released
	while (1) {
		NativeGE_getKeyInput4Ntv(&keys);
		if ((keys.key2 & GE_KEY_O) == 0) break;
	}
	
	// wait for 'O'
	while (1) {
		NativeGE_getKeyInput4Ntv(&keys);
		if (keys.key2 & GE_KEY_O) break;
	}
	
	exit(-1);
}
