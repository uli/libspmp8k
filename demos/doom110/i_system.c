//
// I_GetTime
// returns time in 1/70th second tics
//
int  I_GetTime (void)
{
#ifndef SPMP8
    struct timeval	tp;
    struct timezone	tzp;
    int			newtics;
    static int		basetime=0;
  
    gettimeofday(&tp, &tzp);
    if (!basetime)
	basetime = tp.tv_sec;
    newtics = (tp.tv_sec-basetime)*TICRATE + tp.tv_usec*TICRATE/1000000;
    return newtics;
#else
	#include "libgame.h"

	uint32_t t, dt;
	
	t = NativeGE_getTime();
	t = t - old_time;

	return t;
#endif
}



